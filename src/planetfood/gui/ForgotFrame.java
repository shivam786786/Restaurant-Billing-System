    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         User user = new User();
        int isvalidInput = validateInput();
        if (isvalidInput == 0) {
            JOptionPane.showMessageDialog(null, "All Fields are Mandatory", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;

        }
        else if(isvalidInput == 2) {
            JOptionPane.showMessageDialog(null, "PassWord Should have Atleast 4 characters", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(isvalidInput == 3) {
            JOptionPane.showMessageDialog(null, "Password MissMatching", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String userType = getUserType();
        if (userType == null) {
            JOptionPane.showMessageDialog(null, "Please select user type", "ERROR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try  {
            user.setUserId(userId);
            user.setUsername(name);
            user.setPassword(password);
            user.setUsertype(userType);
            boolean chk = UserDao.updateUser(user);
            if(chk == true) {
               JOptionPane.showMessageDialog(null, "Password update Successfully", "SUCCESS!", JOptionPane.INFORMATION_MESSAGE);
               txtUserId.setText("");
               txtName.setText("");
               rAdmin.setSelected(false);
               rCashier.setSelected(false);
               txtPassword.setText("");
               pRpass.setText("");
               
               
        }
            else {
                JOptionPane.showMessageDialog(null, "No record found \n ALL FIELDS ARE CASE SENSITIVE", "ERROR!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error In DB", "ERROR!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
                    
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

