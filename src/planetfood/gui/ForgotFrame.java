    private int validateInput() {
        userId = txtUserId.getText();
        name = txtName.getText();
        char[] pwd = txtPassword.getPassword();
        char [] pwd1 = pRpass.getPassword();
        
        if (userId.isEmpty() || pwd.length == 0 || pwd1.length == 0) {
            return 0;
        } 
        else if(String.valueOf(pwd).equals(String.valueOf(pwd1)) == false) {
            return 3;
        }
        else if(pwd.length < 4 || pwd1.length < 4) {
            return 2;
        }
        else {
            password = String.valueOf(pwd);
            return 1;
        }

    }
