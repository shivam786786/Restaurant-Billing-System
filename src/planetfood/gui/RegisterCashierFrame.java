    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed
        // TODO add your handling code here:
        int valid = validateInput();
        if(valid == 0) {
            JOptionPane.showMessageDialog(null, "All Fields are Mandatory", "ERROR!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else if (valid == 2) {
            JOptionPane.showMessageDialog(null, "Password Missmatching", "ERROR!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else if (valid == 3) {
            JOptionPane.showMessageDialog(null, "Password should have atleats four characters", "ERROR!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
       try {
           User user = new User();
           Iterator<Employee> it = EmpList.iterator();
           while(it.hasNext()) {
            Employee e = it.next();
            if(e.getEmpId().equals(cmbEmpId.getSelectedItem())) {
             user.setUserId(txtUserId.getText());
             user.setUsername(e.getEmpName());
             user.setPassword(password1);
             user.setEmpId(e.getEmpId());
             user.setUsertype(e.getJob());
             
            boolean chk = UserDao.registerUser(user);
            if(chk == true) {
                JOptionPane.showMessageDialog(null, "Cashier Register SuccessFully","SUCCESS!",JOptionPane.INFORMATION_MESSAGE);
                
            }
            else
                 JOptionPane.showMessageDialog(null, "Cashier is not Register","ERROR!",JOptionPane.INFORMATION_MESSAGE);
            txtUserId.setText("");
            jPassword.setText("");
            jRepass.setText("");
                break;
            }
           }
           cmbEmpId.removeAllItems();
            showId();
           
       } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Error In Register Cashier","ERROR!",JOptionPane.INFORMATION_MESSAGE);
               ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRegActionPerformed

    private void cmbEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbEmpIdActionPerformed
        // TODO add your handling code here:
        if(cmbEmpId.getSelectedItem() == null)
            return;
        String empid = cmbEmpId.getSelectedItem().toString();
        try {
            Employee e = EmployeeDao.getEmployeeById(empid);
            if(e == null) {
                JOptionPane.showMessageDialog(null, "Error no employee found", "ERROR!",JOptionPane.INFORMATION_MESSAGE);
            }
            else {
                txtEmpName.setText(e.getEmpName());
            }
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB", "ERROR!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
      
   
    }//GEN-LAST:event_cmbEmpIdActionPerformed

