    private void showId() {
         try {
             int flag;
               EmpList = UserDao.getEmp();
             userList = UserDao.getUser();
            it = EmpList.iterator();
            
            while(it.hasNext()) {
                flag = 0;
                userId = it.next().getEmpId();
               i =  userList.iterator();
               
                while(i.hasNext()) {
                    userId1 = i.next().getEmpId();
                    if(userId.equals(userId1) == true) {
                        flag = 1;
                        break;
                    }
                }
                if(flag == 0) {
                    cmbEmpId.addItem(userId);  
                }
           }  
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB","ERROR!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
     private int validateInput() {
        
        userId = txtUserId.getText();
        char[] pwd = jPassword.getPassword();
        char [] rpwd = jRepass.getPassword();
        password1 = String.valueOf(pwd);
        password2 = String.valueOf(rpwd);
        if (userId.isEmpty() || pwd.length == 0 || rpwd.length == 0) {
            return 0;
        }
        else if(pwd.length < 4) {
            return 3;
        }
        else if(password1.equalsIgnoreCase(password2) == false) {
            return 2;
        }
       
        else {
            password1 = String.valueOf(pwd);
            return 1;
        }

    }

