    private int validateInput() {
        char[] pwd = jPassword.getPassword();
        String username = txtUserName.getText();
        if(txtEmpId.getText().isEmpty() || txtUserId.getText().isEmpty() || txtUserName.getText().isEmpty() || pwd.length == 0 || txtSal.getText().isEmpty())
            return 0;
        char [] name = username.toCharArray();
        for(char c : name) {
            if(Character.isDigit(c)) {
               JOptionPane.showMessageDialog(null, "Error! Employee Name Should have only characters","ERROR!",JOptionPane.INFORMATION_MESSAGE);
               return 2;
            }
        }
        password = String.valueOf(pwd);
        return 1;
    }
     private String getUserType() {
        if (jrAdmin.isSelected()) {
            return jrAdmin.getText();
        } else if (jrCashier.isSelected()) {
            return jrCashier.getText();
        } else {
            return null;
        }
    }
