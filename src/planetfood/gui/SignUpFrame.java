    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int valid = validateInput();
        if(valid == 0) {
             JOptionPane.showMessageDialog(null, "Error!Please Fill All the Fields", "ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(valid == 2) {
            return;
        }
        User user = new User();
        Employee emp = new Employee();
        try {
            emp.setEmpId(txtEmpId.getText());
            emp.setEmpName(txtUserName.getText());
            emp.setJob(jrAdmin.getText());
            emp.setSal(Double.parseDouble(txtSal.getText()));
            user.setEmpId(txtEmpId.getText());
            user.setUserId(txtUserId.getText());
            user.setUsername(txtUserName.getText());
            user.setPassword(password);
            user.setUsertype(jrAdmin.getText());
            boolean chk = EmployeeDao.addEmployee(emp);
            boolean status = UserDao.registerUser(user);
            if(status == true && chk == true) {
                 JOptionPane.showMessageDialog(null, "User register successfully","SUCCESS!",JOptionPane.ERROR_MESSAGE);
            }
            else {
                 JOptionPane.showMessageDialog(null, "user is not registered","Error",JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error! While connectng in DB","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

