        // TODO add your handling code here:
         int rowIndex = jTable1.getSelectedRow();
         int coloumnIndex = jTable1.getSelectedColumn();
        if(rowIndex == -1){
            JOptionPane.showMessageDialog(null, "Error ! No Item selected", "ERROR!",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Vector v =  (Vector) model.getDataVector().elementAt(rowIndex);
        model.removeRow(rowIndex);
        double price = Double.parseDouble(v.get(2).toString());
        double quant = Double.parseDouble(v.get(3).toString());
        total = total - (quant * price);
        disc = Math.round((dis * total)/100.0);
        subTotal = total - disc;
        txtSubTotal.setText(String.valueOf(subTotal));
        txtDis.setText(String.valueOf(disc));
        gst = Math.round((5 * total)/100.0);
        txtGst.setText(String.valueOf(gst));
        grandTotal = Math.round(subTotal + gst);
