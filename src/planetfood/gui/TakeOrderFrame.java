    public void loadProductDetails() {
         cmbcatName.removeAllItems();
        try {
             categories = CategoryDao.getAllCategory();
            
            for(String catName : categories.keySet()) {
                cmbcatName.addItem(catName);
            }
            do {
                 String in = JOptionPane.showInputDialog("How Much percent discount offered ?");
                 if(in.isEmpty()) {
                     JOptionPane.showMessageDialog(null, "Enter discount","ERROR",JOptionPane.ERROR_MESSAGE);
                     continue;
                 }
                 try {
                     dis = Integer.parseInt(in);
                     lblDis.setText("Discount (" + dis + "%)");
                     break;
                 }catch(NumberFormatException nf) {
            JOptionPane.showMessageDialog(null, "String cannot be converted into number","ERROR",JOptionPane.ERROR_MESSAGE);
        } 
            }while(true);
        }   catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in Connecting DB", "ERROR!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
   }
