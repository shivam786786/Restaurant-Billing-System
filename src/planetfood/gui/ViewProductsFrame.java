    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
        // TODO add your handling code here:
        if(rbtnViewSingleProduct.isSelected()) {
            ViewSingleProductFrame singleProd = new ViewSingleProductFrame();
            singleProd.setVisible(true);
            this.dispose();
        }
        else if(rbtnViewAllProduct.isSelected()) {
            ViewAllProductFrame allProduct = new ViewAllProductFrame();
            allProduct.setVisible(true);
            this.dispose();
            
        }
        else {
            JOptionPane.showMessageDialog(null, "Error! Please Choose a choice", "ERROR!", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnDoTaskActionPerformed

