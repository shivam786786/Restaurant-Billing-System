    private void cmbProductCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProductCatActionPerformed
        // TODO add your handling code here:
        try {
            String catName = cmbProductCat.getSelectedItem().toString();
            String catId = categories.get(catName);
            productList = ProductDao.getSingleProduct(catId);
            cmbProdId.removeAllItems();
            for(String prodId : productList.keySet()) {
                cmbProdId.addItem(prodId);
            }
            
        }   catch (SQLException ex) {
                 JOptionPane.showMessageDialog(null, "Error in Connecting DB", "ERROR!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
    }//GEN-LAST:event_cmbProductCatActionPerformed

