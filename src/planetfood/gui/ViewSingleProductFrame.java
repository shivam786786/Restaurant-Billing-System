    private void loadProductDetails() {
        try {
            categories = CategoryDao.getAllCategory();
            categories.keySet().forEach((catName) -> {
                cmbProductCat.addItem(catName);
            });
        }   catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error in Connecting DB", "ERROR!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
    }
     private boolean validateInput() {
        
       String productName = txtProductName.getText();
       double productPrice = Double.parseDouble(txtProductPrice.getText());
       if(productName == null || productPrice == 0.0) 
           return false;
        else       
           return true;
        
    } 


