    private void cmbProdIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProdIdActionPerformed
        // TODO add your handling code here:
       if(cmbProdId.getSelectedItem() == null)
            return;
        String prodId = cmbProdId.getSelectedItem().toString();
        Product p = productList.get(prodId);
        txtProductName.setText(p.getProdName());
        txtProductPrice.setText(String.valueOf(p.getProdprice()));
        if(p.getIsActive().equalsIgnoreCase("y")) {
            chkIsActive.setSelected(true);
            return;
        }
       chkIsActive.setSelected(false);
    }//GEN-LAST:event_cmbProdIdActionPerformed

